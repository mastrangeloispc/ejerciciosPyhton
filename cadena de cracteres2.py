cadena = 'Roberto'
print(cadena[0]) # muestra el indice 0 (R)

cadena = 'Roberto'
print(cadena[-2]) # muestra el indice -2 (t)

cadena = 'Roberto'
print(cadena[1:4]) # muestra desde el 1 al 4

