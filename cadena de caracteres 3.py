# cadenas de caracteres 3

cadena = 'Hola mundo'.upper() # pasa a mayuscula
print(cadena)

cadena = 'Hola mundo'.lower() # pasa a minuscula
print(cadena)

cadena = 'hola mundo'.capitalize() # primera mayuscula
print(cadena)

cadena = 'hola mundo'.title() # primera letra de todas mayuscula
print(cadena)

cadena = 'hola mundo'.count('o') # cuantas letras o
print(cadena)

cadena = 'hola mundo'.count('hola') # cuantas palabras
print(cadena)

cadena = 'hola mundo'.find('m') # busca indice letra o
print(cadena)

cadena = 'hola mundo'.find('mundo') # busca indice mundo
print(cadena)

cadena = 'hola mundo mundo mundo'.rfind('mundo') # busca de derecha a izquierda
print(cadena)

cadena = "1000".isdigit() # son todos numeros? True o False
print(cadena)

cadena = "qwerty".isalpha() # son todas letras? True o False
print(cadena)

cadena = "10003sada".isalnum() # son todos alfanumericos? True o False
print(cadena)

cadena = "hola mundo".islower() # todo en minuscula?
print(cadena)

cadena = "hola mundo".isupper() # todo en mayuscula?
print(cadena)

cadena = "hola mundo".istitle() # titulo?
cadena = "hola mundo".isspace() # espacios?

