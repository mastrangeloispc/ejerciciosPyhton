# bucle for

coleccion = [1,2,3,4,5,'asas',2,354,65]

for i in coleccion:
    print(f'Elemento: {i}')
    
'''
el bucle for trabaja con listas. Recorre la lista elemento x elemento
por eso pone 5 veces el mensaje

se puede hacer con listas, con tuplas, con conjuntos

en el caso de los diccionarios
'''

cole = {'Roberto':45, 'Romina':45, 'Mateo':14, 'Luca':7}

for i in cole:
    print(f'{cole[i]}') # valor del diccionario
    print(f'{i} ____________ {cole[i]}') # muestra valor y clave
    

    