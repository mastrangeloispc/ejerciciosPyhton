# diccionarios

''' se guardan desordenados.

se pueden poner dentro listas, tuplas, otros diccionarios

Tiene la forma de clave, valor
'''

diccionario = {'azul':'blue', 'rojo':'red', 'verde':'green'}
'''
clave azul
valor blue
'''

diccionario['amarillo'] = 'yellow' # agrega otro elemento, clave valor

diccionario['azul'] = 'BLUE' # modifica el valor

del(diccionario['verde']) # borra un elemento x la clave

print(diccionario['azul']) # pongo la clave y me da el valor


print(diccionario)


